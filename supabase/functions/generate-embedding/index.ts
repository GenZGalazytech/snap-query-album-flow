
// Simulated Edge Function for generating text embeddings
import { serve } from 'https://deno.land/std@0.177.0/http/server.ts';

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

// For demo purposes, this returns mock data
// In a real implementation, this would connect to an embedding model
serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { text } = await req.json();
    
    // Generate a random embedding vector (1536 dimensions is common for OpenAI embeddings)
    const embedding = Array.from({ length: 1536 }, () => Math.random() - 0.5);
    
    return new Response(
      JSON.stringify({
        embedding: embedding,
        text: text,
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      }
    );
  } catch (error) {
    return new Response(
      JSON.stringify({ error: error.message }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 400,
      }
    );
  }
});
